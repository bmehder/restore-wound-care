<script>
  export let item

  const { heading, src, description } = item

  let isBlurred = true

  const handleClick = () => (isBlurred = !isBlurred)
</script>

<li class="flow">
  <h3>{heading}</h3>
  <img
    class="square"
    class:isBlurred
    {src}
    on:click={handleClick}
    on:keydown
    alt={heading}
  />
  <p>{description}</p>
</li>

<style>
  img {
    filter: blur(0);
    transition: filter 600ms ease;
    cursor: pointer;
  }
  .isBlurred {
    filter: blur(var(--size));
  }
</style>
